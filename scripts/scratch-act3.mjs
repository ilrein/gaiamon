const WALK = new Set([".", "p", "g", "f"]);
const tile = (a, x, z) => a[Math.round(z)]?.[Math.round(x)];

function check(name, tiles, w, coords) {
  const probs = [];
  tiles.forEach((r, i) => {
    if (r.length !== w) probs.push(`${name}: row ${i} len ${r.length} != ${w}`);
  });
  for (const [label, x, z] of coords) {
    if (!WALK.has(tile(tiles, x, z)))
      probs.push(`${name}: ${label} (${x},${z}) on '${tile(tiles, x, z)}'`);
  }
  if (probs.length) probs.forEach((p) => console.log("  " + p));
  else console.log(`${name}: OK (${tiles.length} rows x ${w})`);
}

